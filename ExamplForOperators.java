package day3;

public class ExamplForOperators {

	public static void main(String[] args) {
		System.out.println("---------- Arithmetic Operastors ----------");
		
		int num1 = 30;
		int num2 = 40;
		System.out.println("Addition " + (num1 + num2));
		System.out.println("Subtraction" + (num1 - 12));
		System.out.println("Division" + (num2 * 3));
		System.out.println("Multiplication" + (num2 / 10));
		System.out.println("Modulas" + (num2 % 3));
		
	}

}
